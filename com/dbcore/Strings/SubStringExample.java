package com.dbcore.Strings;

public class SubStringExample {
    public static void main(String args[]){

        String s = "Hareesh is learning";
        System.out.println("Atual value");
        System.out.println("after index 5 = " + s.substring(5));
        System.out.println("between 1 to 6 index = " + s.substring(0,6));
    }
}
